    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        
        //para n√£o gravar dados em branco
        if(jTextFieldNomeDivisao.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"digite o nome");
            jTextFieldNomeDivisao.requestFocus();
        }else 
            
        if(flag == 1){
        mod.setNomeDivisao(jTextFieldNomeDivisao.getText());
        mod.setNomeDispositivo((String)jComboBoxDispositivo.getSelectedItem());
        
        //chama o metodo para gravaros dados
        controlo.gravar(mod);
        
        //limpa os campos
        jTextFieldIDdivisao.setText("");
        jTextFieldNomeDivisao.setText("");
        jTextFieldPesquisar.setText("");
        //muda estado dos campos e botoes
        jComboBoxDispositivo.setEnabled(false);
        jButtonGravar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
        
        // atualiza a tabela
        preencherTabela("select * from divisao order by id_divisao");
        
        
        }else{
             mod.setIdDivisao(Integer.parseInt(jTextFieldIDdivisao.getText()));
             mod.setNomeDivisao(jTextFieldNomeDivisao.getText());
             mod.setNomeDispositivo((String)jComboBoxDispositivo.getSelectedItem());
            
             //chama o metodo para alterar os dados
             controlo.alterar(mod);
         
             //limpa os campos
            jTextFieldDivisao.setText("");
            jTextFieldNomeDivisao.setText("");
            jTextFieldPesquisar.setText("");
            
            //muda estado dos campos e botoes
            jComboBoxDispositivo.setEnabled(false);
            jButtonGravar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            
            //atualiza a tabela
            preencherTabela("select * from dispositivo order by id_dispositivo");
        }
    }//GEN-LAST:event_jButtonGravarActionPerformed

    
